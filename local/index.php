                    enableSwiper();
                }
            };

            const enableSwiper = function() {
                // 如果 Swiper 實例不存在，則建立它
                if (mySwiper2 === null) {
                    mySwiper2 = new Swiper(".mySwiper2", swiperOptions);
                }
            };

